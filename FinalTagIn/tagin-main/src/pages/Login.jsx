const Login = ({ setIsLoggedIn, setUserAddress }) => {
  const [web3, setWeb3] = useState(null);
  const [status, setStatus] = useState("Connect with MetaMask to continue");
  const [connecting, setConnecting] = useState(false);
  const navigate = useNavigate();

  useEffect(() => {
    if (window.ethereum) {
      const web3Instance = new Web3(window.ethereum);
      setWeb3(web3Instance);
    } else {
      setStatus("ü¶ä MetaMask not detected. Please install it.");
    }
  }, []);

  const handleLogin = async () => {
    try {
      setConnecting(true);

      // Step 1: Request accounts
      const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
      const account = accounts[0];

      // Step 2: Check network (Sepolia = 0xaa36a7)
      const chainId = await window.ethereum.request({ method: "eth_chainId" });
      if (chainId !== "0xaa36a7") {
        setStatus("‚ùå Please switch MetaMask to Sepolia Testnet");
        setConnecting(false);
        return;
      }

      // Step 3: Recreate Web3 instance fresh
      const web3Instance = new Web3(window.ethereum);
      const contract = new web3Instance.eth.Contract(CONTRACT_ABI, contractAddress);

      setStatus("‚è≥ Checking whitelist...");

      // Step 4: Call whitelist check
      const isWhitelisted = await contract.methods.whitelist(account).call();

      if (isWhitelisted) {
        setStatus("‚úÖ Whitelisted. Redirecting...");
        setIsLoggedIn(true);
        setUserAddress(account);
        setTimeout(() => navigate("/Dashboard"), 1000);
      } else {
        setStatus("‚ùå You are not whitelisted as a manufacturer.");
        setConnecting(false);
      }

    } catch (err) {
      console.error("MetaMask login error:", err);
      setStatus(`‚ö†Ô∏è Login failed: ${err.message}`);
      setConnecting(false);
    }
  };

  return (
    <div className="min-h-screen bg-gradient-to-br from-gray-50 to-gray-100 flex items-center justify-center px-4">
      <div className="w-full max-w-md">
        {/* Card */}
        <div className="bg-white rounded-3xl shadow-xl p-8 md:p-10">
          {/* Logo */}
          <div className="flex justify-center mb-2">
            <h1 className="text-3xl font-extrabold tracking-wide text-black">
              TAG<span className="text-gray-400">-</span>IN
            </h1>
          </div>

          {/* Subtitle */}
          <p className="text-center text-gray-500 text-sm mb-8">
            Sign in to continue
          </p>

          {/* Buttons */}
          <div className="space-y-3">
            {/* Connect MetaMask Button */}
            <button 
              onClick={handleLogin} 
              disabled={connecting || !web3}
              className="w-full bg-black mb-4 hover:bg-gray-900 text-white px-6 py-4 rounded-2xl shadow-xl transition-all font-medium flex items-center justify-center gap-2 disabled:opacity-50 disabled:cursor-not-allowed"
            >
              {connecting ? (
                "Redirecting..."
              ) : (
                <>
                  <FaEthereum size={20} />
                  Connect to MetaMask
                </>
              )}
            </button>

            {/* Back to Home Button */}
            <Link to="/">
              <button className="w-full bg-white hover:bg-gray-50 text-black px-6 py-4 rounded-2xl border border-gray-200 transition-all font-medium">
                Back to home
              </button>
            </Link>
          </div>

          {/* Status Message */}
          <div className="mt-6 text-center text-sm text-gray-600">
            {status}
          </div>
        </div>

        {/* Additional Info (Optional) */}
        <p className="text-center text-gray-500 text-xs mt-6">
          Make sure you're connected to Sepolia Testnet
        </p>
      </div>
    </div>
  );
};

export default Login;