function TransferOwnership() {
  const [tokenId, setTokenId] = useState('');
  const [newOwner, setNewOwner] = useState('');
  const [loading, setLoading] = useState(false);

  const handleTransfer = async () => {
    if (!window.ethereum) {
      toast.error('MetaMask not detected');
      return;
    }

    if (!tokenId || !newOwner) {
      toast.error("Please fill in all fields.");
      return;
    }

    setLoading(true);
    try {
      const web3 = new Web3(window.ethereum);
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      const accounts = await web3.eth.getAccounts();
      const currentOwner = accounts[0];

      const contract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);

      await contract.methods
        .safeTransferFrom(currentOwner, newOwner, tokenId)
        .send({ from: currentOwner });

      await axios.post('http://10.103.1.168:5000/api/transfer', {
        tokenId,
        from: currentOwner,
        to: newOwner,
        timestamp: new Date().toISOString()
      });

      toast.success("✅ Ownership transferred successfully!");
      setTokenId('');
      setNewOwner('');
    } catch (err) {
      console.error(err);
      toast.error("❌ Transfer failed: " + err.message);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-white"
      style={{
        backgroundImage:
          'linear-gradient(90deg, #f7f7f7 1px, transparent 1px), linear-gradient(180deg, #f7f7f7 1px, transparent 1px)',
        backgroundSize: '40px 40px',
        backgroundAttachment: 'fixed'
      }}
    >
      <div className="flex items-center justify-center min-h-screen p-4">
        <div className="w-full max-w-md bg-white shadow-md rounded-xl p-8 border border-gray-200">
          <h1 className="text-2xl font-bold text-center text-gray-800 mb-6">
            Transfer Product Ownership
          </h1>

          <div className="flex flex-col gap-4">
            <input
              type="text"
              placeholder="Enter Product Token ID"
              value={tokenId}
              onChange={(e) => setTokenId(e.target.value)}
              className="w-full p-3 border border-gray-300 rounded focus:outline-none focus:ring-2 focus:ring-gray-400"
            />

            <input
              type="text"
              placeholder="Enter New Owner Wallet Address"
              value={newOwner}
              onChange={(e) => setNewOwner(e.target.value)}
              className="w-full p-3 border border-gray-300 rounded focus:outline-none focus:ring-2 focus:ring-gray-400"
            />

            <button
              onClick={handleTransfer}
              disabled={loading}
              className={`w-full py-3 rounded text-white font-medium transition 
                ${loading ? 'bg-gray-400 cursor-not-allowed' : 'bg-gray-800 hover:bg-gray-900'}`}
            >
              {loading ? 'Transferring...' : 'Transfer Ownership'}
            </button>
          </div>

          <p className="mt-4 text-center text-sm text-gray-500">
            This will update ownership on blockchain and backend.
          </p>
        </div>
      </div>

      <ToastContainer />
    </div>
  );
}

export default TransferOwnership;
