export default function Registerproduct() {
  const [loading, setLoading] = useState(false);
  const [form, setForm] = useState({
    productName: '',
    serial: '',
    model: '',
    type: '',
    color: '',
    date: ''
  });

  const onChange = e => setForm({ ...form, [e.target.name]: e.target.value });

  const handleRegister = async (e) => {
    e.preventDefault();
    setLoading(true);

    const { productName, serial, model, type, color, date } = form;
    const metadata = JSON.stringify({ name: productName, serial, model, type, color, date });
    const metadataHash = SHA256(metadata).toString();

    if (window.ethereum) {
      try {
        const web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: 'eth_requestAccounts' });
        const accounts = await web3.eth.getAccounts();

        const contract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);

        const tx = await contract.methods
          .mintProduct("0x" + metadataHash)
          .send({ from: accounts[0] });

        const tokenId = tx.events.ProductMinted.returnValues[0].toString();
        const details = await contract.methods.getProductDetails(tokenId).call();

        await axios.post('http://10.103.1.168:5000/api/register', {
          name: productName,
          serial,
          model,
          type,
          color,
          date,
          tokenId,
          metadataHash: details[0],
          manufacturer: details[1],
          owner: details[2],
        });

        toast.success("✅ Product registered successfully!");
        setForm({
          productName: '',
          serial: '',
          model: '',
          type: '',
          color: '',
          date: ''
        });
      } catch (err) {
        console.error(err);
        toast.error("❌ Error: " + err.message);
      } finally {
        setLoading(false);
      }
    } else {
      toast.error("Please install MetaMask.");
      setLoading(false);
    }
  };

  return (
    <div className="layout" style={{
      minHeight: '100vh',
      backgroundColor: '#fff',
      backgroundImage: 'linear-gradient(90deg, #f7f7f7 1px, transparent 1px), linear-gradient(180deg, #f7f7f7 1px, transparent 1px)',
      backgroundSize: '40px 40px',
      backgroundAttachment: 'fixed'
    }}>
      <div className="container" style={{
        display: 'flex',
        justifyContent: 'center',
        alignItems: 'center',
        minHeight: '80vh',
        padding: '20px'
      }}>
        <div className="card" style={{
          maxWidth: 550,
          width: '100%',
          margin: '0 auto',
          padding: '24px',
          background: '#fff',
          borderRadius: '12px',
          boxShadow: '0 4px 12px rgba(0,0,0,0.1)'
        }}>
          <div style={{
            fontWeight: 700,
            marginBottom: 16,
            fontSize: 22,
            textAlign: 'center'
          }}>
            Register Product (Bind NFC → Blockchain)
          </div>

          <form
            className="grid"
            style={{ display: 'flex', flexDirection: 'column', gap: '16px' }}
            onSubmit={handleRegister}
          >
            <input className="input" style={{ padding: '10px', width: '100%' }}
              placeholder="Product Name" name="productName" value={form.productName} onChange={onChange} />
            <input className="input" style={{ padding: '10px', width: '100%' }}
              placeholder="Serial Number" name="serial" value={form.serial} onChange={onChange} />
            <input className="input" style={{ padding: '10px', width: '100%' }}
              placeholder="Model Number" name="model" value={form.model} onChange={onChange} />
            <input className="input" style={{ padding: '10px', width: '100%' }}
              placeholder="Product Type" name="type" value={form.type} onChange={onChange} />
            <input className="input" style={{ padding: '10px', width: '100%' }}
              placeholder="Product Color" name="color" value={form.color} onChange={onChange} />
            <input className="input" style={{ padding: '10px', width: '100%' }}
              type="date" placeholder="Date of Manufacture" name="date" value={form.date} onChange={onChange} />

            <div className="right" style={{ display: 'flex', justifyContent: 'center', gap: 12 }}>
              <button className="btn"
                style={{
                  padding: '10px 20px',
                  backgroundColor: '#6b46c1',
                  color: '#fff',
                  border: 'none',
                  borderRadius: '6px',
                  cursor: 'pointer'
                }}
                type="submit" disabled={loading}
              >
                {loading ? "Registering..." : "Register Product"}
              </button>
            </div>
            <div className="muted" style={{ textAlign: 'center', fontSize: '14px', color: '#555' }}>
              This writes the product to blockchain and updates backend database.
            </div>
          </form>
        </div>
      </div>
      <ToastContainer />
    </div>
  );
}
