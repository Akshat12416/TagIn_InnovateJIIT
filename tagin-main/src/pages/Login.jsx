const Login = ({ setIsLoggedIn, setUserAddress }) => {
  const [web3, setWeb3] = useState(null);
  const [status, setStatus] = useState("Connect with MetaMask to continue");
  const [connecting, setConnecting] = useState(false);
  const navigate = useNavigate();

  useEffect(() => {
    if (window.ethereum) {
      const web3Instance = new Web3(window.ethereum);
      setWeb3(web3Instance);
    } else {
      setStatus("ü¶ä MetaMask not detected. Please install it.");
    }
  }, []);

  const handleLogin = async () => {
    try {
      setConnecting(true);
      const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
      const account = accounts[0];
      setStatus("‚è≥ Checking whitelist...");

      const contract = new web3.eth.Contract(CONTRACT_ABI, contractAddress);
      const isWhitelisted = await contract.methods.whitelist(account).call();

      if (isWhitelisted) {
        setStatus("‚úÖ Whitelisted. Redirecting...");
        setIsLoggedIn(true);
        setUserAddress(account);
        setTimeout(() => navigate("/Dashboard"), 1000); // short delay for user feedback
      } else {
        setStatus("‚ùå You are not whitelisted as a manufacturer.");
        setConnecting(false);
      }
    } catch (err) {
      setStatus(`‚ö†Ô∏è Login failed: ${err.message}`);
      setConnecting(false);
    }
  };

  return (
    <div className="container" style={{ maxWidth: 420, paddingTop: 60 }}>
      <div className="card">
        <div style={{ display: "flex", justifyContent: "center", marginBottom: 8, fontWeight: 800, fontSize: 20 }}>
          TAG<span style={{ color: "var(--accent)" }}>.</span>IN
        </div>
        <div className="muted" style={{ textAlign: "center", marginBottom: 16 }}>
          Sign in to continue
        </div>

        <div className="grid" style={{ gap: 12 }}>
          <button 
            className="btn" 
            onClick={handleLogin} 
            disabled={connecting || !web3}
            style={{ justifyContent: "center" }}
          >
            {connecting ? "Redirecting..." : <><FaEthereum className="mr-2" /> Connect to MetaMask</>}
          </button>

          <Link to="/" className="btn btn-outline" style={{ justifyContent: "center" }}>
            Back to home
          </Link>
        </div>

        <div style={{ textAlign: "center", marginTop: 12 }} className="muted">
          {status}
        </div>
      </div>
    </div>
  );
};

export default Login;
